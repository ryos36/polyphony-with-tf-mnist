#@offload
def do_mnist7_mem(a:List[bit16], _mem:List[bit16], lst_len = LEN):
    rom_w = W_PARAM 
    rom_b = B_PARAM 
    mem = [0] * 10

    xi = 0
    for i in range(lst_len):
        x = a[i]
        for j in unroll(range(10)):
            mem[j] = bfloat.mul_add(x, rom_w[xi + j], mem[j])

        xi += 10

    for j in range(10):
        _mem[j] = bfloat.add(mem[j], rom_b[j])
    

